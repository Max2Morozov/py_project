def stroka_up():
    """ Первая ф-я, которая принимает на вход строку и возвращает ее со всеми заглавными
    """
    s = input()
    print(s.upper())
    return

def stroka_title():
    """ функция, которая принимает на вход строку и возвращает ее заглавной буквой.
    """
    d = input()
    print(d.title())
    return

